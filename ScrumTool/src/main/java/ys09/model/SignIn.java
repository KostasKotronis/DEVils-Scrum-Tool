package ys09.model;

public class SignIn {
    String mail;
    String password;

    public String getEmail() {
        return mail;
    }

    public void setEmail(String mail) {
      this.mail = mail;
    }

    public String getPassword() {
      return password;
    }

    public void setPassword(String password) {
      this.password = password;
    }
}
